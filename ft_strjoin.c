#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
{
	size_t	lenght;
	char	*result;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	lenght = ft_strlen(s1) + ft_strlen(s2);
	result = malloc(sizeof(char) * lenght + 1);
	if (!result)
		return (NULL);
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		result[i] = s2[j];
		i++;
		j++;
	}
	result[i] = '\0';
	return (result);
}
